// Mini-Activity1
// Using prompt convert Celcius to Kelvin
// Present the output using alert

let input1 = parseInt(prompt("Enter The Celcius"));

let kelvinConv = input1 + 273.15;
alert("Converted to Kelvin: " + kelvinConv);

// Mini-Activity2
// Using prompt get circle area
// Present the output using alert

let input2 = parseInt(prompt("Enter The Radius"));

let rSquared = input2 * input2;
let areaC = 3.14159265359 * rSquared;

alert("Area of the Circle: " + areaC);
